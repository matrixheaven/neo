use std::path::Path;

pub(super) const REQUIRED_AGENTS_HEADINGS: &[&str] = &[
    "Reference",
    "Project Identity",
    "Development Constitution",
    "Workflow",
    "Git Rules",
    "Third-party Tools, Plugins, MCP",
    "Project Architecture",
    "Technology Choices",
    "Fixed Principles",
    "Subagent Preference",
    "References",
    "Project Documentation",
    "Security",
    "Metadata",
];

